tasks.register("downloadTailwind") {
  val outputFile = file("src/main/assets/tailwind.min.js")
  inputs.property("url", "https://cdn.tailwindcss.com")
  outputs.file(outputFile)
  
  doLast {
    if (!outputFile.exists()) {
      println("Downloading Tailwind CSS...")
      outputFile.parentFile.mkdirs()
      try {
        val connection = URL("https://cdn.tailwindcss.com").openConnection() as HttpURLConnection
        connection.requestMethod = "GET"
        connection.connectTimeout = 15000
        connection.readTimeout = 15000
        connection.inputStream.use { input ->
          outputFile.outputStream().use { output ->
            input.copyTo(output)
          }
        }
        println("Tailwind CSS downloaded successfully to assets/tailwind.min.js")
      } catch (e: Exception) {
        println("Failed to download Tailwind CSS: ${e.message}. Using offline mockup...")
      }
    }
  }
}

tasks.named("preBuild") {
  dependsOn("downloadTailwind")
}
